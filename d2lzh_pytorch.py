def sgd(params, lr, batch_size):
    """使用随机梯度下降更新参数。
    
    参数:
    params: 模型参数列表
    lr: 学习率
    batch_size: 当前批次的样本数量
    """
    for param in params:
        if param.grad is not None:  # 确保梯度存在
            param.data -= lr * param.grad.data / batch_size  # 更新参数
